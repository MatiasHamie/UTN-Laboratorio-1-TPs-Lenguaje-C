int sumaDosNumeros(int numeroUno, int numeroDos)
{
    return numeroUno+numeroDos;
}

int restaDosNumeros(int numeroUno, int numeroDos)
{
    return numeroUno-numeroDos;
}

float multiplicacionDosNumeros(int numeroUno, int numeroDos)
{
    return numeroUno*numeroDos;
}

int divisionDosNumeros(int numeroUno, int numeroDos)
{
    return numeroUno/numeroDos;
}

